// Built-in 5x7 bitmap font for text rendering without external font dependencies.
// Covers ASCII printable characters (32-126). Each glyph is stored as a bitmask.

namespace SharpImage.Drawing;

/// <summary>
/// A simple 5x7 bitmap font embedded in code for text rendering. Each character is defined as 7 rows of 5-bit masks.
/// Suitable for annotations, test labels, and debug overlays.
/// </summary>
public static class BitmapFont
{
    public const int GlyphWidth = 5;
    public const int GlyphHeight = 7;
    public const int GlyphSpacing = 1;
    public const int LineSpacing = 2;

    /// <summary>
    /// Render a string to a list of pixel coordinates (relative to origin).
    /// </summary>
    public static List<(double X, double Y)> RenderString(string text, double scale = 1.0)
    {
        var pixels = new List<(double X, double Y)>();
        double cursorX = 0;
        double cursorY = 0;

        foreach (char c in text)
        {
            if (c == '\n')
            {
                cursorX = 0;
                cursorY += (GlyphHeight + LineSpacing) * scale;
                continue;
            }

            if (c < 32 || c > 126)
            {
                continue;
            }

            var glyph = Glyphs[c - 32];
            for (int row = 0;row < GlyphHeight;row++)
            {
                byte rowBits = glyph[row];
                for (int col = 0;col < GlyphWidth;col++)
                {
                    if ((rowBits & (1 << (4 - col))) != 0)
                    {
                        // For scale > 1, fill the scaled pixel area
                        for (int sy = 0;sy < Math.Max(1, (int)scale);sy++)
                        {
                            for (int sx = 0;sx < Math.Max(1, (int)scale);sx++)
                            {
                                pixels.Add((cursorX + (col * scale) + sx,
                                           cursorY + (row * scale) + sy));
                            }
                        }
                    }
                }
            }

            cursorX += (GlyphWidth + GlyphSpacing) * scale;
        }

        return pixels;
    }

    /// <summary>
    /// Measure the bounding box of a text string.
    /// </summary>
    public static (double Width, double Height) MeasureString(string text, double scale = 1.0)
    {
        double maxWidth = 0, curWidth = 0;
        int lines = 1;

        foreach (char c in text)
        {
            if (c == '\n')
            {
                if (curWidth > maxWidth)
                {
                    maxWidth = curWidth;
                }

                curWidth = 0;
                lines++;
            }
            else
            {
                curWidth += (GlyphWidth + GlyphSpacing) * scale;
            }
        }

        if (curWidth > maxWidth)
        {
            maxWidth = curWidth;
        }

        double height = lines * (GlyphHeight * scale) + (lines - 1) * (LineSpacing * scale);
        return (maxWidth - GlyphSpacing * scale, height);
    }

